# ავტობუსის მაქვს 10 მგზავრი
passengers = 10

# ავტობუსის გასვლაზე ითვლება რამდენი მგზავრი გადავიდა
passengers_left = 0

# სანამ არ დაკავებულია ყველა ადგილი
while passengers_left < passengers:
    # ავტობუსი შევიწყებთ მგზავრების წარმადობით
    passengers_left += 1
    print(f"მგზავრი შევიდა {passengers_left} ადგილზე")
    
# ყოველი დაკავებული ადგილზე იპრინტება "ადგილი დაკავდა"
print("ადგილი დაკავდა")
